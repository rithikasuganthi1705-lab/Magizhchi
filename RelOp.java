class RelOp
{
	public static void main(String[]args)
	{
		int a = 10;
		int b = 20;
		System.out.println("A = " + a);
		System.out.println("B = " + b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
	}
}
		
		
		
		
		
		